public class Exer4 {
    public static void main(String[] args) {
        int dayNumber = 1;
        switch (dayNumber) {
            case 1:
                System.out.println("Понедельник");
                break;
            case 2:
                System.out.println("Вторник");
                break;
            case 3:
                System.out.println("Среда");
                break;
            default:
                System.out.println("Дня с таким номером нет");
        }
        System.out.println("done");
    }
}
